package task_53;

public class Increment_Decrement_Operators {
	
	public static void main(String[] args) {

        int count = 10;

        count++;
        System.out.println(count);

        count--;
        System.out.println(count);
        
        // Website Visitor Counter example
        
        int visitors = 250;

        visitors++;

        System.out.println("Visitors = " + visitors);
    }


}
