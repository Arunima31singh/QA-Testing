package task_53;

public class Relational_Operators {

	public static void main(String[] args) {
		int a = 10;
        int b = 20;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= b);
        System.out.println(a <= b);
        
        // example//
        
        int age = 17;

        System.out.println(age >= 18);

	}

}
